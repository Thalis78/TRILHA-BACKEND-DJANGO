import math

# NUMERO PI
print(f"{math.pi:.2f}")

# ACESSAR NUMERO DE EULER
print(math.e)

# ARREDONDANDO NUMEROS PARA CIMA E PARA BAIXO
num = 10.4
print(math.ceil(num))
print(math.floor(num))

# FATORIAL
num2 = int(input("DIGITE UM NÚMERO:\n"))
print(math.factorial(num2))

# POTÊNCIA
print(math.pow(5,5))

# RAIZ

print(math.sqrt(169))

# MDC
mdc = math.gcd(20,100)
print(mdc)

# LOGARITMO
print(math.log(10))

